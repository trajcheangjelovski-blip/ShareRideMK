-- Нов статус: 'needs_verification' (профилот е под дополнителна верификација).
-- ВАЖНО: ALTER TYPE ... ADD VALUE мора да се изврши САМ (не во трансакција со
-- друг код што веднаш ја користи вредноста). Во Supabase SQL Editor пушти го
-- овој фајл одделно.
-- =====================================================================

alter type user_status add value if not exists 'needs_verification';

-- Значење на статусите:
--   pending            — нов профил, чека прво одобрување (логин да, пристап не)
--   active             — одобрен, целосен пристап
--   needs_verification — може да се логира и прелистува, но НЕ може да поднесува
--                        барања (патник) ниту да става патувања (возач) додека
--                        повторно не се верифицира
--   blocked            — целосен блок: не може ни да се логира
--   deleted            — soft delete: без логин
